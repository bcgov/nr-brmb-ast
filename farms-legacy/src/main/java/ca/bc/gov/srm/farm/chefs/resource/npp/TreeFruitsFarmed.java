package ca.bc.gov.srm.farm.chefs.resource.npp;

import ca.bc.gov.srm.farm.chefs.resource.ChefsResource;

public class TreeFruitsFarmed extends ChefsResource {

	private Boolean apples;
	private Boolean grapes;
	private Boolean cherries;
	private Boolean treefruit;

	public Boolean getApples() {
		return apples;
	}

	public void setApples(Boolean apples) {
		this.apples = apples;
	}

	public Boolean getGrapes() {
		return grapes;
	}

	public void setGrapes(Boolean grapes) {
		this.grapes = grapes;
	}

	public Boolean getCherries() {
		return cherries;
	}

	public void setCherries(Boolean cherries) {
		this.cherries = cherries;
	}

	public Boolean getTreefruit() {
		return treefruit;
	}

	public void setTreefruit(Boolean treefruit) {
		this.treefruit = treefruit;
	}
}
