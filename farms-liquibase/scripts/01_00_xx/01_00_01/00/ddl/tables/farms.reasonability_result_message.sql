CREATE TABLE reasonability_result_message(
    reasonability_result_message_id    numeric(10, 0)    NOT NULL,
    reasonability_test_name            varchar(100)      NOT NULL,
    message                            varchar(4000)     NOT NULL,
    message_type_code                  varchar(10)       NOT NULL,
    reasonability_test_result_id       numeric(10, 0)    NOT NULL,
    revision_count                     numeric(5, 0)     DEFAULT 1 NOT NULL,
    create_user                        varchar(30)       NOT NULL,
    create_date                        timestamp(6)      DEFAULT systimestamp NOT NULL,
    update_user                        varchar(30),
    update_date                        timestamp(6)      DEFAULT systimestamp
) TABLESPACE pg_default
;



COMMENT ON COLUMN reasonability_result_message.reasonability_result_message_id IS 'REASONABILITY RESULT MESSAGE ID is a surrogate unique identifier for REASONABILITY RESULT MESSAGE.'
;
COMMENT ON COLUMN reasonability_result_message.reasonability_test_name IS 'REASONABILITY TEST NAME is the name of the reasonability test that generated this message.'
;
COMMENT ON COLUMN reasonability_result_message.message IS 'MESSAGE is the message text that will be displayed to the user for this reasonability test.'
;
COMMENT ON COLUMN reasonability_result_message.message_type_code IS 'MESSAGE TYPE CODE is a unique code for the object MESSAGE TYPE CODE. Examples of codes and descriptions are ERROR - Error, WARNING - Warning, INFO - Info.'
;
COMMENT ON COLUMN reasonability_result_message.reasonability_test_result_id IS 'REASONABILITY TEST RESULT ID is a surrogate unique identifier for REASONABILITY TEST RESULT.'
;
COMMENT ON COLUMN reasonability_result_message.revision_count IS 'REVISION COUNT is a counter identifying the number of times this record as been modified. Used in the web page access to determine if the record as been modified since the data was first retrieved.'
;
COMMENT ON COLUMN reasonability_result_message.create_user IS 'CREATE USER indicates the user that created the physical record in the database.'
;
COMMENT ON COLUMN reasonability_result_message.create_date IS 'CREATE DATE indicates when the physical record was created in the database.'
;
COMMENT ON COLUMN reasonability_result_message.update_user IS 'UPDATE USER indicates the user that updated the physical record in the database.'
;
COMMENT ON COLUMN reasonability_result_message.update_date IS 'UPDATE DATE indicates when the physical record was updated in the database.'
;
COMMENT ON TABLE reasonability_result_message IS 'REASONABILITY RESULT MESSAGE contains messages generated by reasonability tests.'
;


CREATE INDEX ix_rrm_mtc ON reasonability_result_message(message_type_code)
;
CREATE INDEX ix_rrm_rtri ON reasonability_result_message(reasonability_test_result_id)
;
